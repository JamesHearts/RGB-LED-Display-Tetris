//=============================Get Block Type================================//
//Piece Type : 1 = square, 2 = I, 3 = L, 4 = L mirrored, 5 = N, 6 = N mirrored, 7 = T
//Rotation Number : 0 - 3
//X position
//Y position
uint8_t GetBlockType (int pPiece, int pRotation, int pRow, int pCol)
{
	return mPieces [pPiece][pRotation][pRow][pCol];
}

//=========================Get X Initial Position============================//
//	Returns the vertical displacement for the piece.
//
int GetRowInitialPosition (int pPiece, int pRotation)
{
	return mPiecesInitialPosition [pPiece][pRotation][0]; // 0 - X position.
}

//=========================Get Y Initial Position============================//
//	Returns the horizontal displacement for the piece.
//
int GetColInitialPosition (int pPiece, int pRotation)
{
	return mPiecesInitialPosition [pPiece][pRotation][1]; // 1 - Y position.
}






